function [x_kf_p, P_kf_p] = KF_pred(x_kf_u, P_kf_u, u, Q)
G = [
            1.0,         0,       0.001,         0;
              0,       1.0,           0,     0.001;
              0,    -0.168,        1.00,         0;
              0,     0.574,           0,     0.999;
        ];

        H = [
             0.001;
            -0.002;
             2.059;
            -4.132;
        ];

	x_kf_p = G * x_kf_u + H * u;
    P_kf_p = G * P_kf_u * G' + Q * 0.001;
end

